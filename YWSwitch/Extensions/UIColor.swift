import UIKit

extension UIColor {

    class func YVGreenColor() -> UIColor {
    
        return UIColor(red: 0.36, green: 0.83, blue: 0.06, alpha: 1.00)
    }
    
    class func YVBlueColor() -> UIColor {
    
        return UIColor(red: 0.07, green: 0.33, blue: 0.84, alpha: 1.00)
    }
    
    class func YVLightGreenColor() -> UIColor {
    
        return UIColor(red: 0.94, green: 1.00, blue: 0.91, alpha: 1.00)
    }
    
    class func YVOrangeColor() -> UIColor {
    
        return UIColor(red: 0.99, green: 0.65, blue: 0.01, alpha: 1.00)
    }
    
    class func YVLightBlueColor() -> UIColor {
    
        return UIColor(red: 0.65, green: 0.83, blue: 0.88, alpha: 1.00)
    }
}
